# Property trong C#
## C# sử dụng tính năng gọi là property để cho phép có thể thiết lập lấy giá trị của các trường của class mà không quan tâm phạm vi truy cập của trường đó là gì
- Property cho phép kiểm tra giá trị trước khi gán giá trị
- Property cho phép bảo vệ một trường của lớp bằng cách đọc hoặc ghi dữ liệu thông qua property đó
- Property hỗ trợ đặc tính trừu tượng và đóng gói